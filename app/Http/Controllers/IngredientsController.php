<?php

namespace App\Http\Controllers;

use App\Models\Ingredients;
use App\Models\RecipesIngredients;
use Illuminate\Http\Request;

class IngredientsController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('ingredients.index');
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('ingredients.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data['ingredients_name'] = $request->ingredients_name;
        Ingredients::insert($data);
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $ingredients = Ingredients::findOrFail($id);

        return view('ingredients.edit', compact('ingredients'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $ingredients = Ingredients::findOrFail($id);
        $ingredients->ingredients_name = $request->ingredients_name;
        $ingredients->save();
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $ingredients = Ingredients::findOrFail($id);
        RecipesIngredients::where('ingredients_id', $id)->delete();
        $ingredients->delete();
    }

    public function read()
    {
        $ingredients = Ingredients::all();

        return view('ingredients.read', compact('ingredients'));
    }
}
